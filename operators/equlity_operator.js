console.log("Equality Operator")

//Strict Equality Operator(Datatype + Value)
console.log(1 === 1); // number === number

//output is true

console.log('1' === 1); // String === Number

//output is false

//Lose Equality Operator

console.log(1 == 1); //Number == Number
//output is true 

console.log('1' == 1) //string == Number
//output is true 

console.log(true == '1'); //boolean == Number
//output is true
